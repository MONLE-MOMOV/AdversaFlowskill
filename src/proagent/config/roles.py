def get_role(role_key: str) -> Role:
    """获取角色定义。"""
    if role_key not in ROLES:
        raise KeyError(f"未知角色: {role_key}。可用角色: {list(ROLES.keys())}")
    return ROLES[role_key]


def get_phase_roles(phase: int) -> dict[str, Role]:
    """获取某个阶段的所有角色。"""
    return {k: v for k, v in ROLES.items() if v.phase == phase}
