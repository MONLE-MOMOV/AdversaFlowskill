@dataclass
class Role:
    """AI 角色定义。"""

    name: str
    system_prompt: str
    phase: int  # 主要参与阶段
    is_proposer: bool  # 提案方还是挑战方
    description: str = ""

    @property
    def display_name(self) -> str:
        return self.name


